name=' \t Zar Zar\n  '
print("Original :"+name)
print("Using lstrip:"+name.lstrip())
print("Using rstrip:"+name.rstrip())
print("Using strip:"+name.strip())

#lstrip-> remove whitespace from the left(beginning of the string)
#rstrip-> remove whitespace from the right(end of the string)
#strip-> remove whitespace from both sides